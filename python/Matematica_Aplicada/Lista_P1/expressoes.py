# f) ((2 * 5 < 11) and not (4**2 == 15)) or (9 / 3 > 3)
print(f"f) {((2 * 5 < 11) and not (4**2 == 15)) or (9 / 3 > 3)}")

# g) ((7 + 3 * 2 == 13) and not (2**4 < 10)) or ((9 / 3 == 3) and (8 // 3 == 2))
