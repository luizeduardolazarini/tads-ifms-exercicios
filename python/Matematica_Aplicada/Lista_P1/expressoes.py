# Seção 3: Expressões Lógicas (Resultados True ou False)

# a) not (3 * 4 > 10 and 5 + 2 < 6)
print(f"a) {not (3 * 4 > 10 and 5 + 2 < 6)}")

# b) (2**3 == 8) and not (10 / 2 < 3)
print(f"b) {(2**3 == 8) and not (10 / 2 < 3)}")

# c) not ((2**4 == 16) and (3 * 3 < 8 or 10 / 2 == 5))
print(f"c) {not ((2**4 == 16) and (3 * 3 < 8 or 10 / 2 == 5))}")

# d) (18 // 3 == 2) and (7 / 3 == 1) or not (2**5 < 30)
print(f"d) {(18 // 3 == 2) and (7 / 3 == 1) or not (2**5 < 30)}")

# e) ((5 * 2 > 8) and (9 % 2 == 1)) or not ((3**2 == 9) and (8 / 4 == 2))
